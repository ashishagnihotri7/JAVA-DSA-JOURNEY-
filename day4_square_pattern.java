// Quaction : print square using nestedloop (pattern quaction) of  square

import java.util.Scanner;
public class day4_square_pattern {

    public static void main(String[] args) {
       for(int i=1;i<=5;i++){
           for (int j = 1; j <= 5; j++) {
               System.out.print(" * ");
            }
            System.out.println("*");
       } 
    }
}
// QUACTION: PRINT NUMBER FROM 1 TO 10 using do whilw loop
    
// IT CAN ATLEAT PRINT ONE TIME WHICH IS PRESENT IN DO LOOP 
import java.util.Scanner;
public class day3_do_while_loop {
    public static void main(String[] args) {
        int i = 1;
        do{
            System.out.println("number is "+i);
            i++;
        } while (i <= 10);
  }  
}
